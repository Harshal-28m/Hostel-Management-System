package com.model;

public class Student {

    private int id;
    private String name;
    private String email;
    private String password;
    private String phone;
    private String room_no;
    private String address;
    private String city;
    private String course;
    private String parents_no;
    private String dob;

    public int getId() { return id; }
    public void setId(int id) { this.id = id; }

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getPassword() { return password; }
    public void setPassword(String password) { this.password = password; }

    public String getPhone() { return phone; }
    public void setPhone(String phone) { this.phone = phone; }

    public String getRoom_no() { return room_no; }
    public void setRoom_no(String room_no) { this.room_no = room_no; }

    public String getAddress() { return address; }
    public void setAddress(String address) { this.address = address; }

    public String getCity() { return city; }
    public void setCity(String city) { this.city = city; }

    public String getCourse() { return course; }
    public void setCourse(String course) { this.course = course; }

    public String getParents_no() { return parents_no; }
    public void setParents_no(String parents_no) { this.parents_no = parents_no; }

    public String getDob() { return dob; }
    public void setDob(String dob) { this.dob = dob; }
}